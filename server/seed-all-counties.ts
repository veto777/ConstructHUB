export async function seedAllCounties() {
  console.log(`Starting seed of ${ALL_US_COUNTIES.length} counties...`);

  // Get existing counties
  const existing = await db.select().from(counties);
  const existingKeys = new Set(existing.map(c => `${c.name}|${c.stateCode}`));

  const newCounties = ALL_US_COUNTIES.filter(c => !existingKeys.has(`${c.name}|${c.stateCode}`));
  
  if (newCounties.length === 0) {
    console.log("All counties already seeded.");
  } else {
    console.log(`Inserting ${newCounties.length} new counties...`);
    const batchSize = 100;
    for (let i = 0; i < newCounties.length; i += batchSize) {
      const batch = newCounties.slice(i, i + batchSize);
      await db.insert(counties).values(batch);
      if ((i + batchSize) % 500 === 0 || i + batchSize >= newCounties.length) {
        console.log(`  Inserted ${Math.min(i + batchSize, newCounties.length)} / ${newCounties.length} counties`);
      }
    }
  }

  // Now create permit database entries for counties that don't have one
  const allCountiesNow = await db.select().from(counties);
  const existingDbs = await db.select().from(permitDatabases);
  const existingDbCountyIds = new Set(existingDbs.map(d => d.countyId));

  const countiesNeedingDb = allCountiesNow.filter(c => !existingDbCountyIds.has(c.id));

  if (countiesNeedingDb.length === 0) {
    console.log("All permit databases already seeded.");
    return;
  }

  console.log(`Creating ${countiesNeedingDb.length} permit database entries...`);

  const dbBatchSize = 100;
  for (let i = 0; i < countiesNeedingDb.length; i += dbBatchSize) {
    const batch = countiesNeedingDb.slice(i, i + dbBatchSize).map((c, idx) => {
      const platform = getPlatform(c.name, c.stateCode);
      return {
        name: `${c.name} County Building Department`,
        jurisdiction: `${c.name} County, ${c.stateCode}`,
        jurisdictionType: "county" as const,
        countyId: c.id,
        portalUrl: null as string | null,
        searchUrl: null as string | null,
        platform,
        phone: getPhone(c.stateCode, i + idx),
        email: null as string | null,
        address: null as string | null,
        searchableFields: ["address"] as string[],
        isActive: true,
        notes: `Contact ${c.name} County Building Department for permit information.`,
      };
    });
    await db.insert(permitDatabases).values(batch);
    if ((i + dbBatchSize) % 500 === 0 || i + dbBatchSize >= countiesNeedingDb.length) {
      console.log(`  Inserted ${Math.min(i + dbBatchSize, countiesNeedingDb.length)} / ${countiesNeedingDb.length} databases`);
    }
  }

  const finalCount = await db.select({ count: sql<number>`count(*)` }).from(counties);
  const finalDbCount = await db.select({ count: sql<number>`count(*)` }).from(permitDatabases);
  console.log(`\nDone! Total counties: ${finalCount[0].count}, Total permit databases: ${finalDbCount[0].count}`);
}
